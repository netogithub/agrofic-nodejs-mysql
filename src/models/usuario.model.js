class Usuario {
    constructor(nuevo
    ) {
        this.id_usuario = nuevo.id_usuario,
        this.estatus = nuevo.estatus,
        this.tipo_usuario = nuevo.tipo_usuario,
        this.username = nuevo.username,
        this.pass = nuevo.pass,
        this.nombre = nuevo.nombre,
        this.a_paterno = nuevo.a_paterno,
        this.a_materno = nuevo.a_materno,
        this.descripcion = nuevo.descripcion,
        this.email = nuevo.email,
        this.telefono = nuevo.telefono
    }
}